// Translation function
export function useTranslation() {
  const localeStore = useLocaleStore()
  
  const t = (key: string, params?: Record<string, string | number>): string => {
    const language = localeStore.currentLanguage
    console.log('Translation debug - language:', language, 'key:', key)
    const languageTranslations = translations[language as keyof typeof translations] || translations.en
    console.log('Translation debug - translations found:', !!languageTranslations[key as keyof typeof languageTranslations])
    
    let template = languageTranslations[key as keyof typeof languageTranslations] || key
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        template = template.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v))
      })
    }
    return template
  }
  
  return { t }
}

// Composable for reactive translation
export function useI18n() {
  const localeStore = useLocaleStore()
  
  const t = computed(() => {
    return (key: string, params?: Record<string, string | number>): string => {
      const language = localeStore.currentLanguage
      const languageTranslations = translations[language as keyof typeof translations] || translations.es

      let template = languageTranslations[key as keyof typeof languageTranslations] || key
      if (params) {
        Object.entries(params).forEach(([k, v]) => {
          template = template.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v))
        })
      }
      return template
    }
  })
  
  return { t }
}
